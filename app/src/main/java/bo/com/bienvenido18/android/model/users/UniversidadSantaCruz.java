package bo.com.bienvenido18.android.model.users;

public class UniversidadSantaCruz {
    private String uuid;
    private String coverPhoto;
    private String displayNameS;
    private String addressS;
    private String telefonosS;
    private String carrerasS;
    private String linksS;

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public String getCoverPhoto() {
        return coverPhoto;
    }

    public void setCoverPhoto(String coverPhoto) {
        this.coverPhoto = coverPhoto;
    }

    public String getDisplayNameS() {
        return displayNameS;
    }

    public void setDisplayNameS(String displayNameS) {
        this.displayNameS = displayNameS;
    }

    public String getAddressS() {
        return addressS;
    }

    public void setAddressS(String addressS) {
        this.addressS = addressS;
    }

    public String getTelefonosS() {
        return telefonosS;
    }

    public void setTelefonosS(String telefonosS) {
        this.telefonosS = telefonosS;
    }

    public String getCarrerasS() {
        return carrerasS;
    }

    public void setCarrerasS(String carrerasS) {
        this.carrerasS = carrerasS;
    }

    public String getLinksS() {
        return linksS;
    }

    public void setLinksS(String linksS) {
        this.linksS = linksS;
    }
}
